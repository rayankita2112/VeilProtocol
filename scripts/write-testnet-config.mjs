import fs from "fs";
import path from "path";

const root = process.cwd();
const deployment = JSON.parse(
  fs.readFileSync(path.join(root, "config/testnet-deployment.json"), "utf8"),
);
const localOverridePath = path.join(
  root,
  "config",
  "testnet-deployment.local.json",
);
const localDeployment = fs.existsSync(localOverridePath)
  ? JSON.parse(fs.readFileSync(localOverridePath, "utf8"))
  : {};

const secretValue = (envName, manifestKey) => {
  const fromEnv = process.env[envName];
  if (fromEnv && fromEnv.trim()) return fromEnv.trim();

  const fromLocalManifest = localDeployment[manifestKey];
  if (typeof fromLocalManifest === "string" && fromLocalManifest.trim()) {
    return fromLocalManifest.trim();
  }

  return "";
};

const rootEnv = `# Auto-generated by scripts/write-testnet-config.mjs
STELLAR_NETWORK=${deployment.network}
STELLAR_RPC_URL=${deployment.rpcUrl}
POOL_CONTRACT_ID=${deployment.poolContractId}
VERIFIER_CONTRACT_ID=${deployment.verifierContractId}
SWAP_ROUTER_CONTRACT_ID=${deployment.swapRouterContractId}
TOKEN_CONTRACT_ID=${deployment.tokenContractId}
DEPLOYER_PUBLIC_KEY=${deployment.deployerPublicKey}
DEPLOYER_SECRET_KEY=${secretValue("DEPLOYER_SECRET_KEY", "deployerSecretKey")}
RELAYER_PUBLIC_KEY=${deployment.relayerPublicKey}
RELAYER_SECRET_KEY=${secretValue("RELAYER_SECRET_KEY", "relayerSecretKey")}
SUBMITTER_SECRET_KEY=${secretValue("SUBMITTER_SECRET_KEY", "submitterSecretKey")}
RELAYER_URL=${deployment.relayerUrl}
FRONTEND_ORIGIN=${deployment.frontendOrigin}
RELAYER_PORT=${process.env.RELAYER_PORT || "3002"}
RELAYER_FEE_BPS=${process.env.RELAYER_FEE_BPS || "50"}
TREE_AUTH_SECRET=${process.env.TREE_AUTH_SECRET || ""}
DATABASE_URL=${process.env.DATABASE_URL || ""}
REQUIRE_SUBSET_PROOF=${process.env.REQUIRE_SUBSET_PROOF || "false"}
OFFRAMP_API_KEY=${process.env.OFFRAMP_API_KEY || ""}
SKIP_FRESH_DEPLOY=true
`;

const appEnv = `# Auto-generated by scripts/write-testnet-config.mjs
NEXT_PUBLIC_STELLAR_NETWORK=${deployment.network}
NEXT_PUBLIC_STELLAR_RPC_URL=${deployment.rpcUrl}
NEXT_PUBLIC_POOL_CONTRACT_ID=${deployment.poolContractId}
NEXT_PUBLIC_XLM_POOL_ID=${deployment.poolContractId}
NEXT_PUBLIC_XLM_POOL_100=${deployment.poolContractId}
NEXT_PUBLIC_XLM_TOKEN_ID=${deployment.tokenContractId}
NEXT_PUBLIC_RELAYER_URL=${deployment.relayerUrl}
`;

fs.writeFileSync(path.join(root, ".env"), rootEnv);
fs.writeFileSync(path.join(root, "app", ".env.local"), appEnv);

console.log(
  "Wrote .env and app/.env.local from config/testnet-deployment.json and optional local secret overrides",
);
